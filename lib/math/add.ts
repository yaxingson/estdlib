/**
 * Adds two numbers
 *
 * @since 0.1.2
 * @see {@link https://github.com/yaxingson/estdlib/blob/main/lib/math/add.ts source}
 *
 * @param augend - The first number in an addition
 * @param addend - The second number in an addition
 * @returns Returns the total
 *
 * @example
 *
 * ```js
 * add(6, 4) // => 10
 *
 * ```
 */
export default function add(augend: number, addend: number) {
  return augend + addend
}
